    public void mostrarDadosDoBancoNaTela() {
        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ainda não tem carros cadastrados!", "LISTA", 0);
        } else {
            txtId.setText("" + lista.get(indice).getId());
            txtModelo.setText(lista.get(indice).getModelo());
            cbxMarca.setSelectedItem(lista.get(indice).getMarca());
            cbxAno.setSelectedItem("" + lista.get(indice).getAno());
        }
    }

    public void atualizaLista() {
        lista.clear();
        lista = dao.getCarros();
    }
