    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Carro c = new Carro();

        String modelo = txtModelo.getText();
        String marca = cbxMarca.getSelectedItem().toString();
        int ano = Integer.parseInt(cbxAno.getSelectedItem().toString());

        c.setModelo(modelo);
        c.setMarca(marca);
        c.setAno(ano);

        dao.salvar(c);
        atualizaLista();

        indice = lista.size() - 1;
        mostrarDadosDoBancoNaTela();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Carro c = new Carro();

        int id = Integer.parseInt(txtId.getText());
        String modelo = txtModelo.getText().toString();
        String marca = cbxMarca.getSelectedItem().toString();
        int ano = Integer.parseInt(cbxAno.getSelectedItem().toString());

        c.setId(id);
        c.setModelo(modelo);
        c.setMarca(marca);
        c.setAno(ano);

        dao.alterar(c);
        atualizaLista();

        indice = lista.size() - 1;
        mostrarDadosDoBancoNaTela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int idremove = Integer.parseInt(txtId.getText());
        dao.excluir(idremove);
        atualizaLista();

        indice = lista.size() - 1;
        mostrarDadosDoBancoNaTela();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimeiroActionPerformed
        indice = 0;
        mostrarDadosDoBancoNaTela();
    }//GEN-LAST:event_btnPrimeiroActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        indice--;
        if (indice < 0) {
            indice++;
            JOptionPane.showMessageDialog(null, "Você já está no primeiro registro!", "LISTA", 2);
        } else {
            mostrarDadosDoBancoNaTela();
        }
    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        indice++;
        if (indice > lista.size() - 1) {
            indice--;
            JOptionPane.showMessageDialog(this, "Você já está no último registro!", "LISTA", 2);
        } else {
            mostrarDadosDoBancoNaTela();
        }
    }//GEN-LAST:event_btnProximoActionPerformed

    private void btnUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUltimoActionPerformed
        indice = lista.size() - 1;
        mostrarDadosDoBancoNaTela();
    }//GEN-LAST:event_btnUltimoActionPerformed


